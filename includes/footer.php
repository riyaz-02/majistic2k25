<script>
document.addEventListener('DOMContentLoaded', function() {
    // Get the modal
    var modal = document.getElementById("techTeamModal");
    
    // Get the button that opens the modal
    var btn = document.getElementById("tech-team-link");
    
    // Get the <span> element that closes the modal
    var span = document.getElementsByClassName("tech-team-close")[0];
    
    // When the user clicks the button, open the modal 
    btn.onclick = function(e) {
        e.preventDefault();
        modal.style.display = "block";
        document.body.style.overflow = "hidden"; // Prevent scrolling when modal is open
    }
    
    // When the user clicks on <span> (x), close the modal
    span.onclick = function() {
        modal.style.display = "none";
        document.body.style.overflow = "auto"; // Restore scrolling
    }
    
    // When the user clicks anywhere outside of the modal, close it
    window.onclick = function(event) {
        if (event.target == modal) {
            modal.style.display = "none";
            document.body.style.overflow = "auto"; // Restore scrolling
        }
    }
    
    // Handle image errors by setting default image
    var images = document.querySelectorAll('.tech-team-card img');
    images.forEach(img => {
        img.onerror = function() {
            this.src = 'images/nodp.png';
            this.onerror = null; // Prevent infinite loops
        };
    });
});
</script>




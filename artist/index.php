        document.addEventListener('DOMContentLoaded', function() {
            // Initialize Artist Swiper with unique configuration
            var artistSwiper = new Swiper(".artist-swiper-container", {
                slidesPerView: 1,
                spaceBetween: 30,
                loop: true,
                speed: 800,
                autoHeight: true, /* Added to automatically adjust height */
                keyboard: {
                    enabled: true
                },
                pagination: {
                    el: ".artist-swiper-pagination",
                    clickable: true,
                    dynamicBullets: true
                },
                navigation: {
                    nextEl: ".artist-swiper-button-next",
                    prevEl: ".artist-swiper-button-prev"
                },
                effect: "fade",
                fadeEffect: {
                    crossFade: true
                },
                autoplay: {
                    delay: 5000,
                    disableOnInteraction: false
                },
                on: {
                    init: function() {
                        // Update swiper height after initialization
                        setTimeout(() => {
                            this.updateAutoHeight(10);
                            adjustImageSizing();
                        }, 100);
                    },
                    slideChangeTransitionEnd: function() {
                        // Update height after slide changes
                        this.updateAutoHeight(10);
                        adjustImageSizing();
                    },
                    resize: function() {
                        // Adjust sizes when window is resized
                        adjustImageSizing();
                    }
                }
            });

            // Function to dynamically adjust image sizing based on screen size
            function adjustImageSizing() {
                const banners = document.querySelectorAll('.artist-banner');
                const containers = document.querySelectorAll('.artist-container');
                
                banners.forEach((banner) => {
                    const image = banner.querySelector('.artist-image');
                    const container = banner.closest('.artist-container');
                    
                    // Adjust background-size based on screen width
                    if (window.innerWidth > 1600) {
                        image.style.backgroundSize = 'contain';
                    } else if (window.innerWidth <= 992) {
                        // Force 'cover' on mobile screens
                        image.style.backgroundSize = 'cover';
                    } else {
                        image.style.backgroundSize = 'cover';
                    }
                    
                    // Ensure container height adjusts to banner
                    if (window.innerWidth <= 992) {
                        // Mobile layout (vertical)
                        container.style.height = 'auto';
                        
                        // Ensure the artist-title-container is visible
                        const titleContainer = container.querySelector('.artist-title-container');
                        if (titleContainer) {
                            titleContainer.style.display = 'block';
                        }
                    }
                });
            }
            
            // Initial adjustment
            adjustImageSizing();
            
            // Resize listener
            window.addEventListener('resize', adjustImageSizing);

            // Fade-in animation on scroll
            const artistSection = document.querySelector('#artists-carousel');
            if (artistSection) {
                const observer = new IntersectionObserver(entries => {
                    entries.forEach(entry => {
                        if (entry.isIntersecting) {
                            artistSection.classList.add('artist-visible');
                        } else {
                            artistSection.classList.remove('artist-visible');
                        }
                    });
                }, { threshold: 0.1 });

                observer.observe(artistSection);
            }
        });

        function registerForEvent(eventName) {
            window.open('registration_inhouse.php', '_blank');
            console.log(`Registration initiated for ${eventName}`);
        }
